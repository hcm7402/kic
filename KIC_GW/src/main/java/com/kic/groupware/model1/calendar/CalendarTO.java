package com.kic.groupware.model1.calendar;

public class CalendarTO {
	String cdno;
	String eno;
	String cddiv;
	String deptno;
	String cddivision;
	String cdname;
	String startdate;
	String enddate;
	String contents;
	
	public String getCdno() {
		return cdno;
	}
	public void setCdno(String cdno) {
		this.cdno = cdno;
	}
	public String getEno() {
		return eno;
	}
	public void setEno(String eno) {
		this.eno = eno;
	}
	public String getCddiv() {
		return cddiv;
	}
	public void setCddiv(String cddiv) {
		this.cddiv = cddiv;
	}
	public String getDeptno() {
		return deptno;
	}
	public void setDeptno(String deptno) {
		this.deptno = deptno;
	}
	public String getCddivision() {
		return cddivision;
	}
	public void setCddivision(String cddivision) {
		this.cddivision = cddivision;
	}
	public String getCdname() {
		return cdname;
	}
	public void setCdname(String cdname) {
		this.cdname = cdname;
	}
	public String getStartdate() {
		return startdate;
	}
	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}
	public String getEnddate() {
		return enddate;
	}
	public void setEnddate(String enddate) {
		this.enddate = enddate;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
}
